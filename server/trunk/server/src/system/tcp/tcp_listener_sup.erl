-module(tcp_listener_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/1]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	init/1
]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

%%
%% TODO: Add description of start_link/function_arity
%%
start_link(Port) ->
	supervisor:start_link({local, ?MODULE}, ?SERVER, {10, Port}).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init({AcceptorCount, Port}) ->
	AChild = [
		{tcp_acceptor_sup, {tcp_acceptor_sup, start_link, []},
			transient, infinity, supervisor, [tcp_acceptor_sup]},
		{tcp_listener, {tcp_listener, start_link, [AcceptorCount, Port]},
			transient, 1000, worker, [tcp_listener]}
	],
	{ok, {{one_for_all, 10, 10}, AChild}}.

